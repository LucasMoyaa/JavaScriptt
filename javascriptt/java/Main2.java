// Em Java, as funções (ou métodos) precisam estar dentro de uma classe
public class Main {
    // Criamos um método para mostrar uma saudação
    public static void saudacao() {
        System.out.println("Olá de uma função em Java!");
    }

    public static void main(String[] args) {
        saudacao(); // Chamamos o método
    }
}
