// Exemplo simples de declaração de variável em Java
public class Main {
    public static void main(String[] args) {
        int numero = 10; // Variável do tipo inteiro
        System.out.println("O valor inicial do número é: " + numero);

        // Não é possível mudar o tipo da variável. O exemplo abaixo daria erro:
        // numero = "dez"; // Isso não funciona em Java!
    }
}
